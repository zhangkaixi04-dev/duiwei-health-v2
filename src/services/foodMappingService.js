export const foodMappingService = {
  getIngredients(foodName) {
    if (!foodName) return [];
    
    const normalizedName = foodName.trim().toLowerCase();
    
    for (const [key, ingredients] of Object.entries(foodToIngredients)) {
      if (normalizedName.includes(key.toLowerCase()) || key.toLowerCase().includes(normalizedName)) {
        return ingredients;
      }
    }
    
    return [foodName];
  },
  
  extractFoodVariety(foodLogs) {
    if (!foodLogs || !Array.isArray(foodLogs)) {
      return 0;
    }
    
    const allIngredients = new Set();
    
    foodLogs.forEach(log => {
      const foodName = typeof log === 'string' ? log : log.name;
      if (foodName) {
        const ingredients = this.getIngredients(foodName);
        ingredients.forEach(ing => allIngredients.add(ing));
      }
    });
    
    return allIngredients.size;
  },
  
  getAllIngredients(foodLogs) {
    if (!foodLogs || !Array.isArray(foodLogs)) {
      return [];
    }
    
    const allIngredients = new Set();
    
    foodLogs.forEach(log => {
      const foodName = typeof log === 'string' ? log : log.name;
      if (foodName) {
        const ingredients = this.getIngredients(foodName);
        ingredients.forEach(ing => allIngredients.add(ing));
      }
    });
    
    return Array.from(allIngredients);
  }
};
